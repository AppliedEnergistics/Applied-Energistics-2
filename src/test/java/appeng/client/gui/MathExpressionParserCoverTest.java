    private static void assertInsertions(String expression, int[] validOffsets, int[] invalidOffsets) {
        assertOffsetsCoverExpression(expression, validOffsets, invalidOffsets);

        for (int offset : validOffsets) {
            for (var suffix : suffixes()) {
                var expressionWithSuffix = insert(expression, offset, suffix.text());
                var expectedExpression = multiplyPreviousOperand(expression, offset, suffix.multiplier());
                var expected = parse(expectedExpression);
                var actual = parse(expressionWithSuffix);

                if (expected.isPresent()) {
                    assertTrue(actual.isPresent(), () -> expressionWithSuffix + " should parse as " + expected.get()
                            + " via " + expectedExpression);
                    assertEquals(0, actual.get().compareTo(expected.get()),
                            () -> expressionWithSuffix + " should parse as " + expected.get() + " via "
                                    + expectedExpression + ", but got " + actual.get());
                } else {
                    assertTrue(actual.isEmpty(), () -> expressionWithSuffix + " should fail like " + expectedExpression
                            + ", but got " + actual.get());
                }
            }
        }

        for (int offset : invalidOffsets) {
            for (var suffix : suffixes()) {
                assertFails(insert(expression, offset, suffix.text()));
            }
        }
    }

    private static void assertOffsetsCoverExpression(String expression, int[] validOffsets, int[] invalidOffsets) {
        Set<Integer> seen = new HashSet<>();
        for (int offset : validOffsets) {
            assertTrue(offset > 0 && offset <= expression.length(), () -> "Invalid valid offset " + offset);
            assertTrue(seen.add(offset), () -> "Duplicate offset " + offset + " for " + expression);
        }
        for (int offset : invalidOffsets) {
            assertTrue(offset > 0 && offset <= expression.length(), () -> "Invalid invalid offset " + offset);
            assertTrue(seen.add(offset), () -> "Duplicate offset " + offset + " for " + expression);
        }
        assertEquals(expression.length(), seen.size(), () -> "Missing insertion offset for " + expression);
    }

    private static void assertFails(String expression) {
        var parsed = parse(expression);
        assertTrue(parsed.isEmpty(), () -> expression + " should fail, but got " + parsed.get());
    }

    private static void assertParsesAs(String expression, String expected) {
        var parsed = parse(expression);
        assertTrue(parsed.isPresent(), () -> expression + " should parse as " + expected);
        assertEquals(0, parsed.get().compareTo(new BigDecimal(expected)),
                () -> expression + " should parse as " + expected + ", but got " + parsed.get());
    }

    private static String multiplyPreviousOperand(String expression, int offset, BigDecimal multiplier) {
        int operandStart = findOperandStart(expression, offset);
        return expression.substring(0, operandStart)
                + "("
                + expression.substring(operandStart, offset)
                + "*"
                + multiplier.toPlainString()
                + ")"
                + expression.substring(offset);
    }

    private static int findOperandStart(String expression, int offset) {
        int lastChar = offset - 1;
        if (expression.charAt(lastChar) != ')') {
            int start = lastChar;
            while (start > 0) {
                char previous = expression.charAt(start - 1);
                if (!Character.isDigit(previous) && previous != '.') {
                    break;
                }
                start--;
            }
            return start;
        }

        int depth = 0;
        for (int i = lastChar; i >= 0; i--) {
            char c = expression.charAt(i);
            if (c == ')') {
                depth++;
            } else if (c == '(') {
                depth--;
                if (depth == 0) {
                    return i;
                }
            }
        }
        throw new IllegalArgumentException("Could not find matching '(' in " + expression + " at " + offset);
    }

    private static Optional<BigDecimal> parse(String expression) {
        return MathExpressionParser.parse(expression, createFormat());
    }

    private static String insert(String expression, int offset, String suffix) {
        return expression.substring(0, offset) + suffix + expression.substring(offset);
    }

    private static int[] valid(int... offsets) {
        return offsets;
    }

    private static int[] invalid(int... offsets) {
        return offsets;
    }

    private static List<Suffix> suffixes() {
        var suffixes = new ArrayList<Suffix>();

        addDecimalSuffixes(suffixes, 'k', 1_000L);
        addDecimalSuffixes(suffixes, 'm', 1_000_000L);
        addDecimalSuffixes(suffixes, 'g', 1_000_000_000L);
        addDecimalSuffixes(suffixes, 't', 1_000_000_000_000L);

        addBinarySuffixes(suffixes, 'k', 1L << 10);
        addBinarySuffixes(suffixes, 'm', 1L << 20);
        addBinarySuffixes(suffixes, 'g', 1L << 30);
        addBinarySuffixes(suffixes, 't', 1L << 40);

        return suffixes;
    }

    private static void addDecimalSuffixes(List<Suffix> suffixes, char prefix, long multiplier) {
        suffixes.add(new Suffix(String.valueOf(prefix), BigDecimal.valueOf(multiplier)));
        suffixes.add(new Suffix(String.valueOf(Character.toUpperCase(prefix)), BigDecimal.valueOf(multiplier)));
    }

    private static void addBinarySuffixes(List<Suffix> suffixes, char prefix, long multiplier) {
        for (var prefixCase : List.of(prefix, Character.toUpperCase(prefix))) {
            for (var iCase : List.of('i', 'I')) {
                suffixes.add(new Suffix("" + prefixCase + iCase, BigDecimal.valueOf(multiplier)));
            }
        }
    }

    // Note: based on MathExpressionParserTest.testThird
    private static DecimalFormat createFormat() {
        var format = new DecimalFormat("#.######", DecimalFormatSymbols.getInstance(Locale.US));
        format.setParseBigDecimal(true);
        format.setNegativePrefix("-");
        return format;
    }

