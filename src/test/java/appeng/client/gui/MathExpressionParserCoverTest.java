    @Test
    void suffixesDoNotWorkAsPrefixesOrStandaloneTokens() {
        for (var suffix : suffixes()) {
            assertFails(suffix.text() + "(1+2)");
            assertFails(suffix.text() + "1");
            assertFails("-" + suffix.text());
            assertFails("1 + " + suffix.text() + "2");
            assertFails("1 " + suffix.text() + "+2");
            assertFails("1" + suffix.text() + "10");
            assertFails("1" + suffix.text() + "(10)");
            assertFails("1" + suffix.text() + suffix.text());
        }
    }

